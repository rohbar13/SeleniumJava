package javaprj.operators;

public class ArithmeticOperator {

	public static void main(String[] args) {

		int x = 10;
		
		x++; // x = x+1;
		
		System.out.println(x);
		
		++x;
		
		System.out.println(x);
		
		System.out.println(x++);
		System.out.println(++x);
		
		int y = 10;
		
		y+=10; // y = y+10;
		System.out.println(y);
		y*=10;
		System.out.println(y);
		
		
		
	}

}
